/*
binary search:
-Performance: Binary search is useful for large datasets.
Time complexity O(log n), where n is the number of elements in the list.
-Best Case: O(1) The element is found at the middle of the list.
-Worst Case: O(log n) The element is at one of the ends of the list or not present.
-Average Case: O(log n) On average, binary search eliminates half of the remaining elements in each step.
linear search:
-Performance: Linear search performs well for small datasets
or when the target element is close to the beginning of the list.
Time complexity O(n), where n is the number of elements in the list.
-Best Case: O(1) The element is found at the beginning of the list.
-Worst Case: O(n) The element is at the end of the list or not present in the list.
-Average Case: O(n/2) On average, linear search may need to check half of the elements.
